package io.github.zsgdyf.LoveLabel;

public class LoveLabel {
	private Integer userId;
	private Integer labelId;
	/**
	 * @return the userId
	 */
	public Integer getUserId() {
		return userId;
	}
	/**
	 * @param userId
	 *            the userId to set
	 */
	public void setUserId(Integer userId) {
		this.userId = userId;
	}
	/**
	 * @return the labelId
	 */
	public Integer getLabelId() {
		return labelId;
	}
	/**
	 * @param labelId
	 *            the labelId to set
	 */
	public void setLabelId(Integer labelId) {
		this.labelId = labelId;
	}
}